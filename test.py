#Addition
def add(num1, num2):
    result = num1;
    for i in range(0,num2):
        #print(i)
        result = result +1
    return result
    
answer = add(18,15)    
print(answer)
#Subtraction
def sub(num1, num2):
    result = num1;
    for i in range(0,num2):
        #print(i)
        result = result -1
    return result
    
answer = sub(18,15)    
print(answer)
